        public static async Task Seed(AppDbContext context)
        {
            if (!context.CarAdverts.Any())
            {
                var random = new Random();

                // Данные для повреждений
                var damageTypes = Enum.GetValues<DamageType>();
                var severities = Enum.GetValues<SeverityLevel>();
                var damageDescriptions = new[]
                {
                    "Minor scratches on the hood",
                    "Engine requires replacement",
                    "Dent on rear door",
                    "Windshield crack",
                    "Paint peeling"
                };

                // Генерация 550 машин
                var testCars = Enumerable.Range(1, 550).Select(i =>
                {
                    var car = new CarAdvert
                    {
                        Name = carModels[random.Next(carModels.Length)],
                        Year = random.Next(2010, 2023),
                        Location = locations[random.Next(locations.Length)],
                        MileAge = random.Next(1, 4),
                        Odometr = random.Next(0, 300000),
                        GasType = gasTypes[random.Next(gasTypes.Length)],
                        PhotoUrls = new List<string> { $"https://picsum.photos/{i}" },
                        Damages = new List<CarDamage>()
                    };

                    // Добавляем 1-3 случайных повреждения к каждой машине
                    int damageCount = random.Next(1, 4);
                    for (int j = 0; j < damageCount; j++)
                    {
                        car.Damages.Add(new CarDamage
                        {
                            Type = damageTypes[random.Next(damageTypes.Length)],
                            Severity = severities[random.Next(severities.Length)],
                            Description = damageDescriptions[random.Next(damageDescriptions.Length)]
                        });
                    }

                    return car;
                });

                await context.CarAdverts.AddRangeAsync(testCars);
                await context.SaveChangesAsync();
            }
        }

